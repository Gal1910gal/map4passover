export function genderize(text: string, gender: "female" | "male"): string {
  if (gender === "female") return text;
  let result = text;
  for (const [pattern, replacement] of FEMALE_TO_MALE) {
    result = result.replace(pattern, replacement);
  }
  return result;
}

