/**
 * Hebrew word boundary — matches `word` when:
 *   1. Not preceded by any Hebrew letter (normal word start), OR
 *   2. Preceded by a single Hebrew prefix letter (ו,כ,ב,ל,מ,ש,ה) that itself follows a non-Hebrew char.
 * This allows converting imperatives like "ועשי"→"ועשה", "ותני"→"ותן" etc.
 */
function hw(word: string): RegExp {
  return new RegExp(
    `(?:(?<![א-ת])|(?<=[^א-ת][ובכלמשה]))${word}(?![א-ת])`,
    "g"
  );
}

